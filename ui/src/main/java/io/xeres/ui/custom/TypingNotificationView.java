package io.xeres.ui.custom;

import io.xeres.ui.support.util.UiUtils;
import javafx.fxml.FXML;
import javafx.fxml.FXMLLoader;
import javafx.scene.control.Label;
import javafx.scene.control.ProgressIndicator;
import javafx.scene.layout.HBox;

import java.io.IOException;

import static org.apache.commons.lang3.StringUtils.isEmpty;

public class TypingNotificationView extends HBox
{
	@FXML
	private ProgressIndicator progressIndicator;

	@FXML
	private Label text;

	@FXML
	private WaveDotsView waveDotsView;

	public TypingNotificationView()
	{
		var loader = new FXMLLoader(getClass().getResource("/view/custom/typingnotificationview.fxml"));
		loader.setRoot(this);
		loader.setController(this);

		try
		{
			loader.load();
		}
		catch (IOException e)
		{
			throw new RuntimeException(e);
		}
	}

	public void setText(String text)
	{
		waveDotsView.setVisible(!isEmpty(text));
		this.text.setText(text);
	}

	public void setProgress(String text)
	{
		waveDotsView.setVisible(false);
		this.text.setText(text);
		UiUtils.setPresent(progressIndicator);
	}

	public void stopProgress()
	{
		if (progressIndicator.isManaged())
		{
			UiUtils.setAbsent(progressIndicator);
			text.setText(null);
		}
	}
}
